# Caracteres especiales
print('Hola\nMundo') # \n significa salto de linea
print('\tPython es genial') # \t agrega un tabulador, es decir, tabula lo que esta escrito
print('Juan\' "Perez') # De esta forma podemos agregar comilla simple en un print sin romper la cadena
print("Karla \" Gomez") # De esta forma podemos agregar comillas en un print sin romper la cadena
print('Caracter \\ diagonal invertida') # De esta forma podemos agregar la barra invertida como un caracter especial