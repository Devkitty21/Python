# Manejo de indices en una cadena

cadena1 = "Hola Mundo"
print(cadena1)

# Recuperar el primer caracter

primer_caracter = cadena1[0] # Recupera "H"
print(primer_caracter)
ultimo_caracter = cadena1[9] # Recupera el ultimo caracter "o"
print(ultimo_caracter)

caracter_blanco = cadena1[4]
print(caracter_blanco) # No muestra nada ya que el caracter 4 es un espacio " " pero tambien cuenta como un caracter