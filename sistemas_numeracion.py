# Profundizando Sistemas Numeracion

# Decimal
a = 10
# Binario
a = 0b1010
# Octal
a = 0o12
# Hexadecimal
a = 0xA

# Convertir un tipo entero, incluyendo base
# Base decimal
a = int('23', 10) # Especificamos la base de decimal que en este caso es 10
# Base binario
a = int('10111', 2) # Especificamos la base de binario que en este caso es 2
# Base octal
a = int('27', 8) # Especificamos la base de octal que en este caso es 8
# Base hexadecimal
a = int('17',16) # Especificamos la base de hexadecimal que en este caso es 16
# Base 5
a = int('344',5) # Especificamos la base de 5 que en este caso es 5 (Contiene valores de 0 a 4)
print(f'a: {a}')