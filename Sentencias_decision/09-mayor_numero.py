print('*** El mayor de 2 numeros ***')

# Definimos las variables e inputs
numero1 = int(input('Introduce un numero 1: '))
numero2 = int(input('Introduce otro numero 2: '))

# La condicion de el mayor de 2 numeros

if numero1 > numero2:
    print(f'El numero 1 es mayor: {numero1}')
else:
    print(f'El numero 2 es mayor: {numero2}')