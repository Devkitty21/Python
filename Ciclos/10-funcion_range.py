print('*** Funcion Range ***')

print('Secuencia de numeros del 0 al 4')
# Inicio = 0 (OPCIONAL)
# Fin = 5
# Incremento = 1 (OPCIONAL)
for i in range(5): # fin = 5 -1 = 4
    print(i, end= ' ')

print('\n\nSecuencia de 10 al 20')
# Incremento = 1 (OPCIONAL)
for i in range(10,21): # Tambien se puede poner (10, 20 + 1)
    print(i, end= ' ')

print('\n\n\Secuencia del 20 al 30 de 2 en 2:')
for i in range(20,31,2): # Tambien se puede poner (10, 30 + 1, 2)
    print(i, end=" ")

