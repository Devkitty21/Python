# Ejemplo de tipos de datos en Python

# Entero (int)
edad = 30
print("Edad:", edad)

# Numero con punto flotante (float)
altura = 1.75
print("Altura", altura)

# Cadena de texto (str)
nombre = "Ana"
print('Nombre:', nombre)

# Tipo booleano (bool)
es_estudiante = False
print("Es estudiante?", es_estudiante)

# Ausencia de valor (None)

direccion = None
print("Direccion:",direccion)