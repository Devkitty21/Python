# Operadores aritmeticos

a = 10
b = 3

# Suma +

suma = a + b
print(f'Suma: {suma}')

# Resta -
resta = a - b
print(f'Resta: {resta}')

# Multiplicacion *
multiplicacion = a * b
print(f'Multiplicacion: {multiplicacion}')

# Division / devuelve un tipo float()
division = a / b
print(f'Division: {division:.2f}') # con la expresion ".2f" decimos con el . y el numero de al lado cuantos decimales queremos que nos imprima y la "f" para indicar que es de tipo flotante

# Division entera //
division_entera = a // b
print(f'Division entera: {division_entera}')

# Modulo (%) residuo de la division
modulo = a % b
print(f'Residuo/Modulo de la division: {modulo}')

# Exponente (potencia) **

exponente = a ** b # 10 elevado a 3 10x10x10
print(f'Exponente: {exponente}')